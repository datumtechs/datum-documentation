<li>对于托管存储的原数据（数据存储方式用户可以根据元数据详情中获取），用户需要申请授权访问明文数据权限，获得解密密钥；</li>
<li>用户调用原数据查看或下载api对原数据进行访问；</li>
<li>对于托管存储的原数据，需要使用解密密钥进行解密，获得明文数据；</li>
</ul>
