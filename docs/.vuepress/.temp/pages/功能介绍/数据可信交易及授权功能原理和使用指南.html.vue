<p>概述</p>
<ul>
<li>数据资产化后，即可拥有跟数据对应的NFT，NFT上有元数据信息，用户可以在区块链查看；</li>
<li>NFT中记录了对应的权限信息，如所有权和使用权等；当用户在NFT交易市场上购买指定NFT时，即可获得NFT对应的权限；</li>
<li>Datum提供原始数据的可信托管服务，对于托管的数据，Datum使用代理重加密方式，将数据加密之后存储在托管服务上；用户需要访问明文数据时，只需要申请访问明文数据权限，即可获得数据的解密密钥；</li>
<li>NFT上记录了原数据的存储位置，用户持有NFT在通过验证后，向原数据的存储服务发出查看或下载的请求；</li>
<li>对于托管数据，用户查看和下载的是加密后的原数据，需要通过申请访问明文数据权限获得的解密密钥进行解密，即可获得明文数据；</li>
<li>NFT上记录了原数据的哈希值，用于校验数据的一致性，用户通过持有的NFT即可对下载的原数据进行校验一致性。</li>
</ul>
</li>
</ul>
