<h2 id="什么是数据预言机" tabindex="-1"><a class="header-anchor" href="#什么是数据预言机" aria-hidden="true">#</a> 什么是数据预言机？</h2>
<p>众所周知，区块链中运行的智能合约无法直接获取链外数据或无法直接和外部(链下)系统交互的，这极大程度的限制了作为Web3世界基础设施的Dapp生态或者说Layer2的发展，在Web3的世界里急需丰富多彩的基础设施那么链上的智能合约就必须能够和外部系统交互获取外部数据。Datum数据预言机  (Datum Data Oracle) 就是为此而生，通过Datum数据预言机除了可以使链上合约对接外部系统交互和获取链下数据外，还可以为用户将数据服务进行资产化打包进而为链上合约提供多方协作的联合计算任务能力，使得数据在Web3世界中提现流动性价值和支撑多样化的业务场景。Datum的数据预言机是链下去中心化系统，是基于Datum的底层组成的Datum-network的各个节点间组成的去中心化隐私计算网络。</p>
